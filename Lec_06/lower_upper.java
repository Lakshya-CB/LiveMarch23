package Lec_06;

public class lower_upper {
	public static void main(String[] args) {
		char ch = ',';
		if (ch >= 'a' && ch <= 'z') {
			System.out.println("chota");
		} else if (ch >= 'A' && ch <= 'Z') {

			System.out.println("buddha");
		} else {
			System.out.println("nahi aata!!");
		}
	}
}
